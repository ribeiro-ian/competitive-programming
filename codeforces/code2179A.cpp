#include <bits/stdc++.h>
using namespace std;

void solve() {
    int k, x;
    cin >> k >> x;

    cout << k*x + 1 << endl;
}

int main() {
    ios_base::sync_with_stdio(0); cin.tie(0);

    int tc; cin >> tc;
    while (tc--) solve();

    return 0;
}